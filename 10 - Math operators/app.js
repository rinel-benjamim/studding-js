// Operadores matemáticos
let a = 10;
let b = 5;
console.log(a + b); // Soma
console.log(a - b); // Subtração

console.log(a * b); // Multiplicação
console.log(a / b); // Divisão
console.log(a % b); // Resto da divisão
console.log(a ** b); // Exponenciação
console.log(a++);// Incremento (aumenta o valor de a em 1)
console.log(a--);// Decremento (diminui o valor de a em 1)
console.log(++a);// Incremento pré-fixado (aumenta o valor de a em 1 antes de usá-lo)
console.log(--a);// Decremento pré-fixado (diminui o valor de a em 1 antes de usá-lo)
console.log(-a);// Negação (inverte o sinal de a)
console.log(+a);// Operador de identidade (converte a para um número, se possível)
